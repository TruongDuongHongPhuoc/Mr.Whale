    private void showDetail(int r){
 String id = (String) tbMFQBY.getValueAt(r, 0);
   txtID.setText(id);
   
   String name = (String) tbMFQBY.getValueAt(r, 1);
   cbFood.setSelectedItem(name);
   
   Double quantily = (Double) tbMFQBY.getValueAt(r, 2);
   txtQuantily.setText(quantily.toString());
   
 MFQBYdate m = MFQBYList.get(r);
 
 String Regions = (String) tbMFQBY.getValueAt(r,3);
 cbRegions.setSelectedItem(Regions);
 
 
 Date birth = (Date) XDate.stringToDate(m.getTime());
 DateTime.setDate(birth);
 
 Boolean IAndE  = (Boolean) tbMFQBY.getValueAt(r, 4);
 setBtnGroup(IAndE.toString());
 }
 private void setBtnGroup(String value){ // kiem tra gia tri cha ve  
 for (Component c : jPanel2.getComponents()){
     
 if(c instanceof JRadioButton){
     
     JRadioButton btn = (JRadioButton) c;
     
     if(btn.getActionCommand().equals(value)){
         
      btn.setSelected(true);
     }
   }
 }
}
 
    
    private void addMFQBY(){
     String ID = txtID.getText();
     String name = cbFood.getSelectedItem().toString();
     Double quantily = Double.parseDouble(txtQuantily.getText());
     Boolean ExportImport = Boolean.parseBoolean(GrExportImport.getSelection().getActionCommand());
     String Regions = cbRegions.getSelectedItem().toString();
     
     Date birth = DateTime.getDate();
   String date = XDate.DateToString(birth);
   
   MFQBYdate m = new MFQBYdate(ID, name, quantily, ExportImport, Regions,date); 
   MFQBYList.add(m);
 }
  private void saveFile(){
      XFile.writeObject(FilePath, MFQBYList);
   }
 
  
