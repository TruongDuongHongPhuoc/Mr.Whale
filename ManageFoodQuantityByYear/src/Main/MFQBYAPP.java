private void SortCanByID(){
    Collections.sort(MFQBYList,new Comparator<MFQBYdate>(){
        @Override
        public int compare(MFQBYdate o1, MFQBYdate o2) {
            return o1.getID().compareTo(o2.getID());//
        }
    
    });
}
    
private void sortCanByQuantily (){
Collections.sort(MFQBYList,new Comparator<MFQBYdate>(){

            @Override
            public int compare(MFQBYdate o1, MFQBYdate o2) {
               return o1.getQuantily()<o2.getQuantily()?-1:1;//
            }
        });
}
    
    
