        private void saveFile(){
        try{
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream(filePath));
            oos.writeObject(StudentList);
            oos.flush();
            oos.close();
        }catch (FileNotFoundException e){
            System.err.println("File not found");
        }catch (IOException ex){
            System.err.println("Fail");
        }
    }
        private void delete(){
        if(chosenRow > -1){
            int c = JOptionPane.showConfirmDialog(this, "Do you want to delete",
                    "Confirmation", JOptionPane.YES_NO_OPTION
            , JOptionPane.WARNING_MESSAGE);
            if(c == JOptionPane.YES_OPTION){
                StudentList.remove(chosenRow);
                fillToTable();
                saveFile();
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Please choose someone");
        }
    }
    private void addStudent(){
        if(txtStudentID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Student ID required!");
        }
        if(txtClass.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Student Class required!");
        }
        if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Student Name required!");
        }
        else{
            Student st = new Student(txtStudentID.getText(), txtName.getText(), 
                    cbAddress.getSelectedItem().toString(),txtClass.getText(),rbMale.isSelected(),cbSubject.getSelectedItem().toString());
            StudentList.add(st);
  }
    }
