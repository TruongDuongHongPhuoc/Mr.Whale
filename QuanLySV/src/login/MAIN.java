    private void fillToTable(){
      tblModel.setRowCount(0);
      for(Student e: StudentList){
          Object [] row = new Object[]{
         e.getID(), e.getName(), e.getAddress(),e.getClassStudent(),e.getGender(),e.getSubject()};
          tblModel.addRow(row);
      }
    }
    //set comlumns name of table
    private void initable(){
        String [] columnName = {"ID","Name","Address","Class","Gender","Subject"};
        tblModel = new DefaultTableModel (columnName,0);
        tblStudent.setModel(tblModel);
    }
      private boolean loadFile() throws IOException{
          FileInputStream fis;
          ObjectInputStream ois;
        try{
            fis = new FileInputStream(filePath);
            ois = new ObjectInputStream(fis);
            StudentList = (ArrayList< Student>) ois.readObject();
            ois.close();
            fis.close();
            return true;
        }catch(FileNotFoundException ex){
            System.err.println("FileNotFoundException");
        }catch(IOException ex){
          System.err.println("IOException");
        }catch(ClassNotFoundException ex){
          System.err.println("ClassNotFoundException");
        }
        return false;
    }
